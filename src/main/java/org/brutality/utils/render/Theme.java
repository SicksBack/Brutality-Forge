package org.brutality.utils.render;

import java.awt.Color;

public class Theme {

    public static int getGradientStartColor(Object value) {
        // Dummy implementation for start color based on the value.
        return Color.RED.getRGB();
    }

    public static int getGradientEndColor(Object value) {
        // Dummy implementation for end color based on the value.
        return Color.BLUE.getRGB();
    }
}
